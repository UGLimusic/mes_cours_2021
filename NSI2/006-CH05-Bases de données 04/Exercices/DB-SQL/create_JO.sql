BEGIN TRANSACTION;
DROP TABLE IF EXISTS Sport;
CREATE TABLE Sport
(
    id_sport INTEGER PRIMARY KEY,
    nom_sport VARCHAR(30)
);

DROP TABLE IF EXISTS Discipline;
CREATE TABLE Discipline
(
    id_disc   INTEGER PRIMARY KEY,
    nom_disc  VARCHAR(50),
    type_disc CHAR(1) CHECK (type_disc IN ('I', 'T')), -- un caractère fixe
    id_sport  INTEGER NOT NULL REFERENCES Sport (id_sport) -- id_du sport de la discipline
);

DROP TABLE IF EXISTS Pays;
CREATE TABLE Pays
(
    cio      CHAR(3) PRIMARY KEY, -- code à 3 lettres pour désigner le pays
    iso2     CHAR2(2),            -- code ISO à 2 lettres
    iso3     CHAR(3),             -- code ISO à 3 lettres
    nom      VARCHAR(50),
    capitale VARCHAR(30),
    region   VARCHAR(30)
);
DROP TABLE IF EXISTS Sportif;
CREATE TABLE Sportif
(
    id_sportif INTEGER PRIMARY KEY,
    prenom     VARCHAR(30),
    nom        VARCHAR(30),
    cio        CHAR(3) NOT NULL REFERENCES Pays (cio),
    sexe       CHAR(1) CHECK (sexe IN ('M', 'F'))
);
DROP TABLE IF EXISTS Gagner;
CREATE TABLE Gagner
(
    id_disc    INTEGER REFERENCES Discipline (id_disc),
    id_sportif INTEGER REFERENCES Sportif (id_sportif),
    medaille   CHAR(1) CHECK (medaille IN ('G', 'S', 'B')),
    UNIQUE (id_disc, id_sportif),
    PRIMARY KEY (id_disc, id_sportif, medaille)
);

DROP TABLE IF EXISTS Avoir_lieu;
CREATE TABLE Avoir_lieu
(
    id_disc   INTEGER REFERENCES Discipline (id_disc),
    date_evenement TEXT CHECK (date_evenement BETWEEN ('2012-07-27') AND ('2012-08-12')),
    PRIMARY KEY (id_disc, date_evenement)
);

